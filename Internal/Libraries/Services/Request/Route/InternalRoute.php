    //  @param  variadic ...$function
    //  @return void
    //
    //--------------------------------------------------------------------------------------------------------
    public function restore($ips, String $uri = NULL) : InternalRoute
    {
        $this->restore['ips'] = (array) $ips;
        $this->restore['uri'] = $uri;

        return $this;
    }

