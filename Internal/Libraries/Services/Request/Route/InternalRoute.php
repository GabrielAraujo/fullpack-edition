    // Filter -> 4.3.2
    //--------------------------------------------------------------------------------------------------------
    //
    //  @param  variadic ...$function
    //  @return void
    //
    //--------------------------------------------------------------------------------------------------------
    public function filter()
    {
        $this->_csrf();
        $this->_restore();
        $this->_method();
        $this->_usable();
    }

